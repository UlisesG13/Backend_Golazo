from typing import Protocol

from src.modules.auth.domain.models import AuthUser
from src.modules.usuarios.domain.models import UserModel, DireccionModel, DeviceToken

class UserPort(Protocol):
    async def get_all(self) -> list[UserModel]: ...

    async def get_by_id(self, usuario_id: str) -> UserModel | None: ...

    async def get_by_email(self, email: str) -> UserModel | None: ...

    async def update(self, usuario_id: str, user: UserModel) -> UserModel: ...

    async def delete(self, usuario_id: str) -> None: ...

    async def anonymize_and_soft_delete(self, usuario_id: str) -> None: ...

    async def create_admin(self, user: AuthUser) -> UserModel: ...

    async def get_all_admins(self) -> list[UserModel]: ...

class DireccionPort(Protocol):
    async def get_direcciones_by_usuario_id(self, usuario_id: str) -> list[DireccionModel]: ...

    async def get_direccion_by_id(self, direccion_id: int, usuario_id: str ) -> DireccionModel | None: ...

    async def create_direccion(self, direccion: DireccionModel) -> DireccionModel: ...

    async def update_direccion(self, direccion_id: int, direccion: DireccionModel, usuario_id: str) -> DireccionModel | None: ...

    async def delete_direccion(self, direccion_id: int, usuario_id: str) -> None: ...

    async def set_primary(self, direccion_id: int, usuario_id: str) -> None: ...


class DeviceTokenPort(Protocol):

    async def save(self, user_id: str, token: str) -> None:
        ...

    async def get_by_user(self, user_id: int) -> list[DeviceToken]:
        ...

    async def get_all(self) -> list[DeviceToken]:
        ...

    async def delete(self, token: str) -> None:
        ...
